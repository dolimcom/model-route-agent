def set_cell_text(cell, text, bold=False):
    cell.text = ""
    paragraph = cell.paragraphs[0]
    run = paragraph.add_run(text)
    run.bold = bold
    run.font.size = Pt(10.5)


def set_page_number(section):
    footer = section.footer
    paragraph = footer.paragraphs[0]
    paragraph.text = "第 "
    run = paragraph.add_run()
    fld_char_begin = OxmlElement("w:fldChar")
    fld_char_begin.set(qn("w:fldCharType"), "begin")
    instr_text = OxmlElement("w:instrText")
    instr_text.set(qn("xml:space"), "preserve")
    instr_text.text = "PAGE"
    fld_char_end = OxmlElement("w:fldChar")
    fld_char_end.set(qn("w:fldCharType"), "end")
    run._r.append(fld_char_begin)
    run._r.append(instr_text)
    run._r.append(fld_char_end)
    paragraph.add_run(" 页")


def add_bullets(document, items):
    for item in items:
        document.add_paragraph(item, style="List Bullet")


def add_question_group(document, title, questions, start_index):
    document.add_heading(title, level=1)
    index = start_index
    for question, focus, answer in questions:
        paragraph = document.add_paragraph()
        q_run = paragraph.add_run(f"Q{index:02d}. {question}")
        q_run.bold = True
        q_run.font.size = Pt(11)

        focus_paragraph = document.add_paragraph()
        focus_paragraph.add_run("考察点：").bold = True
        focus_paragraph.add_run(focus)

        answer_paragraph = document.add_paragraph()
        answer_paragraph.add_run("标准回答：").bold = True
        answer_paragraph.add_run(answer)
        index += 1
    return index


