$lines = New-Object System.Collections.Generic.List[string]
$lines.Add("# Semantic Router V7 Stress Dataset")
$lines.Add("")
$lines.Add("Generated from 125 held-out base intents and eight discourse variations per intent.")
$lines.Add("")
$id = 1
foreach ($entry in $datasets.GetEnumerator()) {
    $lines.Add("## $($entry.Key)")
    $lines.Add("")
    foreach ($baseQuestion in $entry.Value) {
        foreach ($wrapper in $wrappers) {
            $lines.Add("$id. $([string]::Format($wrapper, $baseQuestion))")
            $id++
        }
    }
    $lines.Add("")
}

